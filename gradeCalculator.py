perc = float(input("Input what you got here  "))

if perc<60:
    print("You got an F. Boooooooooo. You should feel bad.")
    
elif 60<=perc<70:
    print("You got a D. Did you even study?, or are you stupid?")
    
elif 70<=perc<80:
    print("You got a C. Not impressive.")
    
elif 80<=perc<90:
    print("You got a B. Not bad, but I've seen better")
    
else:
    print("Congrats. You got an A. NOW GO BACK TO STUDYING YOU LAZY INGRATE!")
    

